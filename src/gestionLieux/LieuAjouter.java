    private void btnEnvoyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnvoyerActionPerformed
        // test qui vérifie que les champs sont remplis

        if (txtNom.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, "Il faut saisir le nom du lieu");
        } else {

            if (txtRue.getText().compareTo("") == 0) {
                JOptionPane.showMessageDialog(null, "Il faut saisir la rue du lieu");
            } else {
                if (txtCodePostal.getText().compareTo("") == 0) {
                    JOptionPane.showMessageDialog(null, "Il faut saisir le code postal du lieu");
                } else {
                    if (txtVille.getText().compareTo("") == 0) {
                        JOptionPane.showMessageDialog(null, "Il faut saisir la ville du lieu");
                    } else {
                        
                        //recherche du dernier id de lieu donné
                        int idMax = 0;
                        for (Lieu leLieu : NezDamesSerialisation.lesLieux) {
                            if (leLieu.getIdLieu() > idMax) {
                                idMax = leLieu.getIdLieu();
                            }
                        }
                        // ajout d'un lieu à la collection lesLieux
                        NezDamesSerialisation.lesLieux.add(new Lieu(idMax + 1, txtNom.getText(), txtRue.getText(), txtVille.getText(), Integer.parseInt(txtCodePostal.getText())));
                        JOptionPane.showMessageDialog(null, "Lieu ajouté.");

                        this.dispose();
                    }
                }
            }
        }
    }//GEN-LAST:event_btnEnvoyerActionPerformed

